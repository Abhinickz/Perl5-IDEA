	public boolean isOpeningQuoteFor(int currentState, char currentChar, int... states)
	{
		for (int state : states)
			if (state == currentState)
				return !Character.isWhitespace(currentChar)
						&& (currentChar != '#' || allowSharpQuote);

		return false;
	}


